# Розрахунок абонплати за комунальним лічильникам газу:

old_data = float(input("Введіть попередні показання лічильника, кВт\n ->> "))
new_data = float(input("Введіть теперешні показання лічильника, кВт\n ->> "))
price = float(input("Введіть тариф, грн./кВт\n ->> "))

# Використано кВт, грн.:
used = new_data - old_data
# Потрібно сплатити, грн.:
coast = used * price
print('Використано, кВт.:')
print(float(used))
print('Потрібно сплатити, грн.:')
print(float(coast))