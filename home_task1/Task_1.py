# Розрахунок абонплати за комунальним лічильникам газу:

a = float(input("Введіть попередні показання лічильника ->> "))
b = float(input("Введіть теперешні показання лічильника ->> "))
c = float(input("Введіть тариф, грн. ->> "))

# Потрібно сплатити, грн.:
d = (b - a) * c
print('Потрібно сплатити, грн.:')
print(float(d))